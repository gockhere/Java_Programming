package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello students\nToday we have Java class");
        System.out.println("-----------------------------------------------");
        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch");
        System.out.println("--------------------------------------------------");
        System.out.println("\\");
        System.out.println("--------------------------------------------------");
        System.out.println("My favorite tv show is \"The Walking Death\"");



    }




}



/*
1. \n : New line
2. \t : paragraph space
3.\\ : back slash
4.\" :double quote

 */