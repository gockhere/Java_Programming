package day14_forLoop;

public class ForLoopIntro {
    public static void main(String[] args) {



        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");


        // 10 times repeat

        System.out.println("=========================================");

        for (int i =1; i < 10; i ++ ){ //1,2,3,...10
            System.out.println("Hello World");

        }




    }
}
